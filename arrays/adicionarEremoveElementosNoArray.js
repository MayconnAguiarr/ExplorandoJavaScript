var emails = [];

// Adicionando elementos

emails.push('Maycon@gmail.com');

console.log(emails);

emails.push('Amanda@hotmail.com');

console.log(emails);

//adicionar no inicio do array
emails.unshift('Marlene@hotmail.com');

console.log(emails);


// Removendo elementos

emails.pop();

console.log(emails);

emails.shift();

console.log(emails);

